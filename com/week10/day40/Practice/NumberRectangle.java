package com.week10.day40.Practice;

public class NumberRectangle {
   public static void main(String[] args) {
    int rows=4;
    int coloums=5;
    for(int i=1;i<=rows;i++){
        for(int j=1;j<=coloums;j++){
            System.out.print(j+" ");
        }
        System.out.println();
    }
   } 
}
